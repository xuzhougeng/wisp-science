pub fn tf(locale: Locale, key: &str, vars: &[(&str, &str)]) -> String {
    let mut s = t(locale, key);
    for (k, v) in vars {
        s = s.replace(&format!("{{{k}}}"), v);
    }
    s
}

pub fn tab_count(locale: Locale, base: &str, n: usize) -> String {
    if n == 0 {
        t(locale, base)
    } else {
        tf(locale, &format!("{base}_n"), &[("n", &n.to_string())])
    }
}

pub fn localize_backend(locale: Locale, msg: &str) -> String {
    match msg {
        "API URL is required." => t(locale, "err.api_url_required"),
        "Model is required." => t(locale, "err.model_required"),
        "API key is required." => t(locale, "err.api_key_required"),
        "No API key set. Open Settings and paste your provider API key." => {
            t(locale, "err.no_api_key")
        }
        "Validation timed out after 30s" => t(locale, "err.validation_timeout"),
        "Validation succeeded" => t(locale, "status.validation_succeeded"),
        "Wait for the session to finish its turn, approval, or review before transferring it." => {
            t(locale, "err.session_transfer_busy")
        }
        "Source and target projects must be different." => {
            t(locale, "err.session_transfer_same_project")
        }
        "Target project not found." => t(locale, "err.session_transfer_target_missing"),
        "Wait for every task in this project to finish before synchronizing."
        | "Wait for every task and run in this project to finish before synchronizing." => {
            t(locale, "err.sync_busy")
        }
        "Configure a sync relay URL in Settings first." => t(locale, "err.sync_relay_url"),
        "Configure the relay token in Settings before synchronizing." => {
            t(locale, "err.sync_relay_token")
        }
        "Choose a shared sync folder in Settings first." => t(locale, "err.sync_folder"),
        "The shared project is not available yet. Wait for the cloud folder or relay to finish updating." => {
            t(locale, "err.sync_not_ready")
        }
        "Synchronize this project successfully before copying its device code." => {
            t(locale, "err.sync_code_before_first")
        }
        "selected folder has no SKILL.md" => t(locale, "err.skill_no_md"),
        "select a skill folder or a SKILL.md file" => t(locale, "err.skill_pick"),
        "SKILL.md has no frontmatter (--- block)" => t(locale, "err.skill_no_frontmatter"),
        "SKILL.md frontmatter is not closed with ---" => {
            t(locale, "err.skill_frontmatter_unclosed")
        }
        "SKILL.md is missing a description" => t(locale, "err.skill_no_description"),
        m if m.starts_with("a skill named '") && m.ends_with("' already exists") => {
            let name = &m["a skill named '".len()..m.len() - "' already exists".len()];
            tf(locale, "err.skill_exists", &[("name", name)])
        }
        m if m.starts_with("Validated ") => {
            if let Some(rest) = m.strip_prefix("Validated ") {
                if let Some((provider, model)) = rest.split_once(" with ") {
                    return tf(
                        locale,
                        "status.validated",
                        &[("provider", provider), ("model", model)],
                    );
                }
            }
            msg.to_string()
        }
        _ => msg.to_string(),
    }
}

