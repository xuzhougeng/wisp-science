use leptos::*;
use serde::{Deserialize, Serialize};

#[derive(Clone, Copy, PartialEq, Eq, Default, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum Locale {
    #[default]
    En,
    Zh,
}

impl Locale {
    pub fn from_code(s: &str) -> Self {
        if s.trim().eq_ignore_ascii_case("zh") || s.starts_with("zh-") || s.starts_with("zh_") {
            Self::Zh
        } else {
            Self::En
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Self::En => "en",
            Self::Zh => "zh",
        }
    }

    pub fn detect_browser() -> Self {
        web_sys::window()
            .and_then(|w| w.navigator().language())
            .map(|l| Self::from_code(&l))
            .unwrap_or_default()
    }
}

pub fn set_document_lang(locale: Locale) {
    if let Some(doc) = web_sys::window().and_then(|w| w.document()) {
        if let Some(html) = doc.document_element() {
            let _ = html.set_attribute("lang", locale.code());
        }
    }
}

pub const EMPTY_TITLE_COUNT: usize = 7;
pub const EMPTY_SUBTITLE_COUNT: usize = 7;

pub fn t(locale: Locale, key: &str) -> String {
    lookup(locale, key).unwrap_or(key).into()
}

pub fn empty_title(locale: Locale, index: usize) -> String {
    t(
        locale,
        &format!("empty.title.{}", index % EMPTY_TITLE_COUNT),
    )
}

pub fn empty_subtitle(locale: Locale, index: usize) -> String {
    t(
        locale,
        &format!("empty.subtitle.{}", index % EMPTY_SUBTITLE_COUNT),
    )
}

